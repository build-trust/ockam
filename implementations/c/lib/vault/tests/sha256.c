/**
 * @brief   Common unit test function for SHA256 using Ockam Vault
 * @param   state   Contains a pointer to shared data for all SHA-256 test cases.
 */
void test_vault_sha256(void** state)
{
  ockam_error_t                    error                                        = OCKAM_ERROR_NONE;
  test_vault_sha256_shared_data_t* test_data                                    = 0;
  size_t                           length                                       = 0;
  uint8_t                          sha256_digest[TEST_VAULT_SHA256_DIGEST_SIZE] = { 0 };

  test_data = (test_vault_sha256_shared_data_t*) *state;

  if (test_data->test_count >= test_data->test_count_max) {
    fail_msg("Test count %d has exceeded max test count of %d", test_data->test_count, test_data->test_count_max);
  }

  error = ockam_vault_sha256(test_data->vault,
                             (&(g_sha256_data[test_data->test_count].msg[0])),
                             (g_sha256_data[test_data->test_count].len / 8),
                             &sha256_digest[0],
                             TEST_VAULT_SHA256_DIGEST_SIZE,
                             &length);
  assert_int_equal(error, OCKAM_ERROR_NONE);
  assert_int_equal(length, TEST_VAULT_SHA256_DIGEST_SIZE);

  assert_memory_equal(
    &(g_sha256_data[test_data->test_count].digest[0]), &sha256_digest[0], TEST_VAULT_SHA256_DIGEST_SIZE);
}

/**
 * @brief   Common unit test teardown function for SHA256 using Ockam Vault
 * @param   state[in] Contains a pointer to shared data for all SHA-256 test cases.
 */
int test_vault_sha256_teardown(void** state)
{
  test_vault_sha256_shared_data_t* test_data = 0;

  test_data = (test_vault_sha256_shared_data_t*) *state;
  test_data->test_count++;

  return 0;
}

/**
 * @brief   Triggers SHA-256 unit tests using Ockam Vault.
 * @return  Zero on success. Non-zero on failure.
 */
int test_vault_run_sha256(ockam_vault_t* vault, ockam_memory_t* memory)
{
  ockam_error_t                   error        = OCKAM_ERROR_NONE;
  int                             rc           = 0;
  char*                           test_name    = 0;
  uint16_t                        i            = 0;
  uint8_t*                        cmocka_data  = 0;
  struct CMUnitTest*              cmocka_tests = 0;
  test_vault_sha256_shared_data_t shared_data;

  error = ockam_memory_alloc_zeroed(
    memory, (void**) &cmocka_data, (TEST_VAULT_SHA256_TEST_CASES * sizeof(struct CMUnitTest)));
  if (error != OCKAM_ERROR_NONE) {
    rc = -1;
    goto exit;
  }

  cmocka_tests = (struct CMUnitTest*) cmocka_data;

  shared_data.test_count     = 0;
  shared_data.test_count_max = TEST_VAULT_SHA256_TEST_CASES;

  shared_data.vault  = vault;
  shared_data.memory = memory;

  for (i = 0; i < TEST_VAULT_SHA256_TEST_CASES; i++) {
    error = ockam_memory_alloc_zeroed(memory, (void**) &test_name, TEST_VAULT_SHA256_NAME_SIZE);
    if (error != OCKAM_ERROR_NONE) {
      rc = -1;
      goto exit;
    }

    snprintf(test_name, TEST_VAULT_SHA256_NAME_SIZE, "SHA-256 Test Case %02d", i);

    cmocka_tests->name          = test_name;
    cmocka_tests->test_func     = test_vault_sha256;
    cmocka_tests->setup_func    = 0;
    cmocka_tests->teardown_func = test_vault_sha256_teardown;
    cmocka_tests->initial_state = &shared_data;

    cmocka_tests++;
  }

  if (error != OCKAM_ERROR_NONE) {
    rc = -1;
    goto exit;
  }

  cmocka_tests = (struct CMUnitTest*) cmocka_data;

  rc = _cmocka_run_group_tests("SHA-256", cmocka_tests, shared_data.test_count_max, 0, 0);

exit:
  return rc;
}
