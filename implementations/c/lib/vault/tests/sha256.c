/**
 ********************************************************************************************************
 *                                          TestVaultSha256()
 *
 * @brief   Common unit test function for SHA256 using Ockam Vault
 *
 * @param   state   Contains a pointer to shared data for all SHA-256 test cases.
 *
 ********************************************************************************************************
 */

void TestVaultSha256(void **state) {
  OckamError err = kOckamErrorNone;
  TestVaultSha256SharedData *p_test_data = 0;
  uint8_t sha256_digest[32] = {0};

  /* -------------------------- */
  /* Test Data and Verification */
  /* -------------------------- */

  p_test_data = (TestVaultSha256SharedData *)*state;

  if (p_test_data->test_count >= p_test_data->test_count_max) {
    fail_msg("Test count %d has exceeded max test count of %d", p_test_data->test_count, p_test_data->test_count_max);
  }

  /* ------------ */
  /* SHA-256 Test */
  /* ------------ */

  err = p_test_data->p_vault->Sha256(p_test_data->p_vault_ctx, (&(g_sha256_data[p_test_data->test_count].msg[0])),
                                     (g_sha256_data[p_test_data->test_count].len / 8), &sha256_digest[0], 32);
  assert_int_equal(err, kOckamErrorNone);

  assert_memory_equal(&(g_sha256_data[p_test_data->test_count].digest[0]), &sha256_digest[0], 32);
}

